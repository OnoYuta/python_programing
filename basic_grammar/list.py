# list
l = [1, 20, 3, 40, 6]
# >>> l[0]
# 1
# >>> l[-1]
# 6
# >>> l[1:3]
# [20, 3]
# >>> l[:3]
# [1, 20, 3]
# >>> l[3:]
# [40, 6]
# >>> type(l)
# <type 'list'>

