# リストの操作
s = ['a', 'b', 'c', 'd', 'e', 'f', 'g']
# >>> s[0] = 'X'
# ['X', 'b', 'c', 'd', 'e', 'f', 'g']

# 一部のみ取り出し
# >>> s[2:5] = ['C', 'D', 'E']
# ['X', 'b', 'C', 'D', 'E', 'f', 'g']
# >>> s[2:5] = []
# ['X', 'b', 'f', 'g']

# 要素の追加や削除
# >>> s.append('x')
# ['X', 'b', 'f', 'g', 'x']
# >>> s.insert(0, 200)
# [200, 'X', 'b', 'f', 'g', 'x']
# >>> s.pop()
# 'x'
# >>> s.pop(0)
# 200
# >>> s
# ['X', 'b', 'f', 'g']

# 要素の削除
# >>> del s[0]
# >>> s
# ['b', 'f', 'g']
# 任意の要素を削除
# >>> s.remove('f')
# >>> s
# ['b', 'g']

