# 参照渡し
i = [1, 2, 3, 4, 5]
j = i
j[0] = 100
print('i =', i)
print('j =', j)
# i = [100, 2, 3, 4, 5]
# j = [100, 2, 3, 4, 5]
print('i.id =', id(i))
print('j.id =', id(j))
# 同じIDが出力される
# i.id = 140443418909888
# j.id = 140443418909888

# 値渡し
x = [1, 2, 3, 4, 5]
y = x.copy()
y[0] = 100
print('x =', x)
print('y =', y)
# x = [1, 2, 3, 4, 5]
# y = [100, 2, 3, 4, 5]
print('x.id =', id(x))
print('y.id =', id(y))
# 異なるIDが出力される
# x.id = 140443420522368
# y.id = 140443420522560